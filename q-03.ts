
//Name Cases: Store a person’s name in a variable,
// and then print that person’s name in lowercase, uppercase,
//  and titlecase.

const Name : string = "ayesha";

//It shows the name in small letters
console.log(Name.toLowerCase());

//It shows the name in big letters
console.log(Name.toUpperCase());

//It showes the firs letter of nam big
console.log(Name.charAt(0). toUpperCase() + Name.slice(1). toLowerCase());



